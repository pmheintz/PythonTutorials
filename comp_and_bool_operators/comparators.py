"""
== -> Equality
!= -> Not equal to
< -> Less than
> -> Greater than
<= -> Less than/equal to
>= -> Greater than/equal to
"""

bool_one = 10 == 10 # True
not_equal = 10 != 11 # True
less_than = 10 < 11 # True
greater_than = 10 > 9 # True
lt_eq = 10 <= 10 # True
gt_eq = 10 >= 11 - 1 # True
print(bool_one)
print(not_equal)
print(less_than)
print(greater_than)
print(lt_eq)
print(gt_eq)
